
public class Switch {
	public static void main(String[] args) {
		int velue = 6;
		switch(velue) {
		
		case 1 :
			System.out.println("case1");
			break;
			
		case 2 :
			System.out.println("case2");
			break;
			 
		case 3 :
			System.out.println("");
			break;
			
		default:
			System.out.println(" By Default !!");
			break;
		}
		
		System.out.println("");
	}
	

}
